// create your loops here.
console.log('Start incrementing by one');
// for loop running from 0-9
for (var i = 0; i < 10; i++) {
  console.log('value of i: ' + i);
}

console.log('Start incrementing by two');
// for loop with ten iterations incrementing by two
for (var j = 0; j < 20; j += 2) {
  console.log('value of j: ' + j);
}

console.log('Start decrementing from 100');
// for loop counting back from 100
for (var k = 100; k > 0; k--) {
  console.log('Time till explosion: ' + k);
}
